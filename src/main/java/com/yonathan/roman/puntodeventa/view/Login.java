    private void login() {
        if (!txtUsuario.getText().isEmpty() && !String.valueOf(txtPassword.getPassword()).isEmpty()) {
            boolean inSesion = controller.login(txtUsuario.getText(), String.valueOf(txtPassword.getPassword()));
            if (inSesion) {
                this.setVisible(false);
                this.dispose();
                Principal p = new Principal();
                p.setVisible(true);
            } else {
                JOptionPane.showMessageDialog(this, "Usuario o contraseña incorrecta", "Información", JOptionPane.ERROR_MESSAGE);
            }
        } else {
            txtUsuario.putClientProperty(OUTLINE, txtUsuario.getText().isEmpty() ? OUTLINE_ERROR : null);
            txtPassword.putClientProperty(OUTLINE, String.valueOf(txtPassword.getPassword()).isEmpty() ? OUTLINE_ERROR : null);
        }
    }

